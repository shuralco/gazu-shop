    /** @return array{0:float,1:float}|null [lat, lng] for a (possibly prefixed) city name */
    public static function coordsFor(?string $city): ?array
    {
        $key = self::normalize($city);

        return $key !== '' ? (self::COORDS[$key] ?? null) : null;
    }

    private static function normalize(?string $city): string
    {
        $s = trim(mb_strtolower((string) $city));
        // strip leading settlement-type prefixes: "м.", "м ", "смт", "с.", "село", "м. "
        $s = preg_replace('/^(м\.?|смт\.?|с\.?|село|місто)\s+/u', '', $s) ?? $s;
        $s = trim($s);

        return $s;
    }
