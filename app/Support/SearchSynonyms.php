<?php

namespace App\Support;

/**
 * Колоквіальні ↔ каталожні синоніми для пошуку автозапчастин.
 *
 * Проблема: користувач шукає «масло», а каталог зве товар «оливний фільтр» /
 * «моторна олива» → LIKE '%масло%' дає 0. Так само росіянізми (тормоз, фильтр,
 * подшипник) не збігаються з українськими назвами в каталозі.
 *
 * expand() приймає сирий запит і повертає ДОДАТКОВІ підрядки, за якими теж
 * треба зробити LIKE. Працює за входженням підрядка (substring), тож
 * «моторне масло» теж матчить ключ «масло».
 *
 * Використовується у App\Services\Gazu\CatalogQuery::applySearch() (головний
 * пошук) та App\Http\Controllers\Gazu\StoreController::searchSuggest()
 * (autocomplete) — єдине джерело правди.
 */
class SearchSynonyms
{
    /**
     * key (lowercase substring у запиті) => [додаткові підрядки для LIKE].
     * Підрядки навмисно скорочені до спільного кореня («олив», «гальм»),
     * щоб ловити всі словоформи (олива/оливний/оливне).
     */
    private const MAP = [
        // колоквіальне укр → каталожне укр
        'масло'      => ['олив'],
        'олив'       => ['масло'],
        'дворник'    => ['щітк', 'склоочис'],
        'щітк'       => ['дворник'],
        'покришк'    => ['шин'],

        // росіянізми → українські каталожні назви
        'тормоз'     => ['гальм'],
        'гальм'      => ['тормоз'],
        'фильтр'     => ['фільтр'],
        'подшипник'  => ['підшипник'],
        'підшипник'  => ['подшипник'],
        'ремень'     => ['ремін', 'ремен'],
        'свеча'      => ['свічк'],
        'свічк'      => ['свеч'],
        'аккумулятор' => ['акумулятор'],
        'акумулятор' => ['аккумулятор'],
        'стекло'     => ['скло'],
        'масляный'   => ['олив'],
        'тосол'      => ['антифриз', 'охолодж'],
        'антифриз'   => ['тосол', 'охолодж'],
        'колодк'     => ['накладк'],
        'лампочк'    => ['лампа', 'led'],
    ];

    /**
     * @return string[] додаткові підрядки для LIKE (без оригіналу).
     */
    public static function expand(string $term): array
    {
        $lower = mb_strtolower(trim($term));
        if ($lower === '') {
            return [];
        }

        $extra = [];
        foreach (self::MAP as $key => $syns) {
            if (str_contains($lower, $key)) {
                foreach ($syns as $s) {
                    $extra[] = $s;
                }
            }
        }

        return array_values(array_unique($extra));
    }
}
